public class H01_Pazar02 {
    public static void main(String[] args) {

        //DATA CASTING


        byte num1=10;
        short num2=num1;
        System.out.println(num2); //10
        float num3=num1; //10.0
        double num4=num1;//10.0

        System.out.println(num3);
        System.out.println(num4);

        double num5=146;
        byte num6=(byte) num5;
        System.out.println(num6);//-110



    }
}
